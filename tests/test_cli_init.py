# ---------------------------------------------------------------------------


def test_generic_writes_to_stdout(chdir, capsys):
    rc = _run("init", "generic")
    captured = capsys.readouterr()
    assert rc == 0
    assert captured.out.startswith("<!-- generated by `keys init generic`")
    # No files created.
    assert list(chdir.iterdir()) == [chdir / ".git"]


