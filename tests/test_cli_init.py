def test_aider_creates_conventions_md(chdir, capsys):
    rc = _run("init", "aider")
    captured = capsys.readouterr()
    assert rc == 0
    f = chdir / "CONVENTIONS.md"
    assert f.exists()
    content = f.read_text(encoding="utf-8")
