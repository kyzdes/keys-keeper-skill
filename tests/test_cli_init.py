@pytest.mark.skipif(os.name == "nt", reason="chmod permission bits behave differently on Windows")
def test_init_unwriteable_target_path_returns_error(chdir, capsys):
    """A read-only parent directory should fail cleanly, not crash."""
    locked = chdir / "locked"
    locked.mkdir()
    locked.chmod(0o500)
    try:
        rc = _run("init", "cursor", "--out", str(locked / "foo.mdc"))
        captured = capsys.readouterr()
        assert rc == 2
        assert "error:" in captured.err
        # readable, not a raw traceback
        assert "Traceback" not in captured.err
        assert "permission denied" in captured.err.lower() or "permission" in captured.err.lower()
    finally:
        # Restore writability so pytest can clean up.
        locked.chmod(0o700)


def test_init_out_pointing_at_directory_returns_error(chdir, capsys):
    """`--out` targeting an existing directory should fail with a friendly
    message, not 'IsADirectoryError' bubbling up."""
    a_dir = chdir / "iam-a-dir"
    a_dir.mkdir()
    rc = _run("init", "cursor", "--out", str(a_dir))
    captured = capsys.readouterr()
    assert rc == 2
    assert "error:" in captured.err
    assert "Traceback" not in captured.err
    assert "directory" in captured.err.lower()


def test_init_aider_malformed_marker_does_not_corrupt(chdir):
    """If CONVENTIONS.md has an orphan BEGIN marker (no matching END),
    init must not crash and must produce a file with at least one valid
    BEGIN/END pair (a follow-up init will be idempotent against it)."""
    f = chdir / "CONVENTIONS.md"
    f.write_text(
        f"# user content\n\n{render.MARKER_BEGIN}\n## old stuff with no end marker\n",
        encoding="utf-8",
        newline="\n",
    )
    rc = _run("init", "aider")
    assert rc == 0
    out = f.read_text(encoding="utf-8")
    # User content survives.
    assert "# user content" in out
    # At least one valid BEGIN/END pair now exists (the freshly appended section).
    assert render.MARKER_END in out
    # Second run should now be idempotent (we have a clean pair to splice).
    second = _run("init", "aider")
    assert second == 0


def test_init_claude_malformed_frontmatter_falls_back_to_defaults(chdir):
    """If skills/keys-keeper/SKILL.md has unparseable frontmatter (no
    closing fence), claude render must fall back to canonical defaults
    rather than crashing or preserving garbage."""
    target = chdir / "skills" / "keys-keeper" / "SKILL.md"
    target.parent.mkdir(parents=True)
    # Frontmatter opens with `---` but never closes.
    target.write_text(
        "---\nname: leftover\ndescription: leftover\nthis-fence-never-closes\n# body\n",
        encoding="utf-8",
        newline="\n",
    )
    rc = _run("init", "claude", "--force")
    assert rc == 0
    out = target.read_text(encoding="utf-8")
    # Fence regex requires a closing `---\n`; missing → defaults restored.
    assert "name: keys-keeper" in out
    assert "macOS Keychain" in out  # default description text
    assert "leftover" not in out
    assert "Storage CLI is `keys`" in out  # canonical body
