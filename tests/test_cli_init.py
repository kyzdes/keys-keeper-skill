"""End-to-end tests for `keys init <target>`.

Covers: stdout / single-file / marker-append write modes, --force,
--check, marker-append idempotency, no-.git-warning, aider stderr hint.
"""
from __future__ import annotations

import os
from pathlib import Path

import pytest

from keys_keeper.cli import main as cli_main
from keys_keeper.agent_rules import render


@pytest.fixture
def chdir(tmp_path, monkeypatch):
    """Run each test in an isolated tmp cwd with a git dir present
    (so the no-.git warning is suppressed by default)."""
    monkeypatch.chdir(tmp_path)
    (tmp_path / ".git").mkdir()
    return tmp_path


def _run(*argv: str) -> int:
    return cli_main(list(argv))


